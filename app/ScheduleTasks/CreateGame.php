    /**
     * @throws \JsonException
     */
    public function __invoke()
    {
        $template = array_rand(self::TEMPLATES);
        $coords = array_merge(...self::TEMPLATES[$template]);
        $tileVariants = ceil(count($coords) / 4);
        shuffle($coords);
        $i = 0;
        while ($i < count($coords)) {
            for ($j = 0; $j < $tileVariants; $j++) {
                if ($i === count($coords)) {
                    break;
                }
                $coords[$i][] = $j;
                $coords[$i + 1] = $j;
                $i += 2;
            }
        }

        Game::create(['layout' => $coords]);
    }
